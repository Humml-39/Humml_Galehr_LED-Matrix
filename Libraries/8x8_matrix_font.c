void write2MAX7219(char adress, char data)
{
	for (int i=0; i<132; i++);
	PORTB &= ~(1<<PB4); // SS auf 0
	SPDR = adress; //Adresse übetragen
	while(!(SPSR & (1<<SPIF))); //warten bis Übetragung fertig
	SPDR = data; //Daten übetragen
	while(!(SPSR & (1<<SPIF))); //warten bis Übetragung fertig
	for (int i=0; i<132; i++);
	PORTB |= (1<<PB4);
}
void halfwrite2MAX7219(char adress, char data)
{
	SPDR = adress; //Adresse übetragen
	while(!(SPSR & (1<<SPIF))); //warten bis Übetragung fertig
	SPDR = data; //Daten übetragen
	while(!(SPSR & (1<<SPIF))); //warten bis Übetragung fertig

}


void Matrix_init(int intensity, int shutdown) //Intensity: 0...F (0...16), shutdown: 0/1 (1->normal mode)
{
	DDRB |= (1<<PB5)|(1<<PB7)|(1<<PB4); //MOSI (M->S)(DIN), SCL (CLK), SS(CS)  -> Ausgang
	SPCR |= (1<<SPE)|(1<<MSTR)|(1<<SPR0);
		
	write2MAX7219(0x09,0x00); //no decode mode, weil decode Mode für 7seg
	write2MAX7219(0x0A,intensity); //Intensity/Helligkeit | 0x07->15/32
	write2MAX7219(0x0B,0x07); //Scan Limit --> 0x07 -> alle Digits
	write2MAX7219(0x0C,shutdown); //Shutdown
}

void multiMatrix_init(int intensity, int shutdown, int NUMofMatrix) //Intensity: 0...F (0...16), shutdown: 0/1 (1->normal mode)
{
	DDRB |= (1<<PB5)|(1<<PB7)|(1<<PB4); //MOSI (M->S)(DIN), SCL (CLK), SS(CS)  -> Ausgang
	SPCR |= (1<<SPE)|(1<<MSTR)|(1<<SPR0);
	PORTB &= ~(1<<PB4); // SS auf 0
	for (int i=0; i<132; i++);
	for (int i=0; i<NUMofMatrix; i++){
		halfwrite2MAX7219(0x09,0x00); //no decode mode, weil decode Mode für 7seg
	}
	write2MAX7219(0x09,0x00); //no decode mode, weil decode Mode für 7seg
	_delay_us(1);
	PORTB &= ~(1<<PB4); // SS auf 0
	for (int i=0; i<132; i++);
	for (int i=0; i<NUMofMatrix; i++){
		halfwrite2MAX7219(0x0A,intensity); //Intensity/Helligkeit | 0x07->15/32
	}
	write2MAX7219(0x0A,intensity); //Intensity/Helligkeit | 0x07->15/32
	_delay_us(1);
	PORTB &= ~(1<<PB4); // SS auf 0
	for (int i=0; i<132; i++);
	for (int i=0; i<NUMofMatrix; i++){
		halfwrite2MAX7219(0x0B,0x07); //Scan Limit --> 0x07 -> alle Digits
	}
	write2MAX7219(0x0B,0x07); //Scan Limit --> 0x07 -> alle Digits
	_delay_us(1);
	PORTB &= ~(1<<PB4); // SS auf 0
	for (int i=0; i<132; i++);
	for (int i=0; i<NUMofMatrix; i++){
		halfwrite2MAX7219(0x0C,shutdown); //Shutdown
	}
	write2MAX7219(0x0C,shutdown); //Shutdown
	_delay_us(1);
}

void Matrix_intensity(int intensity, int NUMofMatrix)
{
	for (int i=0; i>=NUMofMatrix; i++){
		halfwrite2MAX7219(0x0A,intensity);
	}
	write2MAX7219(0x0A,intensity); //Intensity/Helligkeit | 0x07->15/32
}
void draw2Matrix(int row0, int row1, int row2, int row3, int row4, int row5, int row6, int row7)
{
	write2MAX7219(0x01, row7);
	write2MAX7219(0x02, row6);
	write2MAX7219(0x03, row5);
	write2MAX7219(0x04, row4);
	write2MAX7219(0x05, row3);
	write2MAX7219(0x06, row2);
	write2MAX7219(0x07, row1);
	write2MAX7219(0x08, row0);
}

